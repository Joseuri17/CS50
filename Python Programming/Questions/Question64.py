numdays = input("Enter a number in the range of 1 through 7:")
day=int(numdays)
if (day == 1):
    print("Monday.")
elif day == 2:
    print("Tuesday.")
elif day == 3:
    print("Wedneday.")
elif day == 4:
    print("Thursday.")
elif day == 5:
    print("Friday.")
elif day == 6:
    print("Saturday.")
elif day == 7:
    print("Sunday.")
else:
    print("Error")